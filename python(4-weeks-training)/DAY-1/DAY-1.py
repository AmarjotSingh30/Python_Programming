# 3)Logical Operators(and, or, not)
x=78
y=56
print((x > y) and (x == 78) and (y < 100))  # True if all conditions are true))
print((x > y) or (x == 98) or (y < 60))  # True if all conditions are true))
print(not(x > y))

# 4)Membership Operators(in, not in)
